#include<stdio.h>
#pragma warning(disable : 4996)

int search(char *str)
{
	int c = 0;

	while (*str)
	{
		if (*str == 'a')
		{
			c++;

		}
		str++;

	}

	return c;

}

int main(void)
{
	char str[100];
	int ans;

	printf("문자열을 입력하세요.\n");
	scanf("%s", &str);

	ans = search(str);

	printf("%s 안에 a는 %d개가 있습니다.\n", str, ans);

	return 0;

}